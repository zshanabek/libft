#include "libft.h"

int		ft_intlen(intmax_t n)
{
	int len;

	len = 0;
	if (n < 0)
		len += 1;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
