#include "../printf.h"

int		ft_valid(char c)
{
	if (c != 'l' && c != 'h' && c != 'z' && c != 'j' && c != '+' && c != '-'
		&& c != '#' && !ft_isdigit(c) && c != '*' && c != ' ' && c != '_' &&
		c != '.')
		return (0);
	return (1);
}
