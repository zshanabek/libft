#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t siz)
{
	char		*d;
	size_t		n;
	size_t		dlen;
	char		*s;

	d = dst;
	s = (char *)src;
	n = siz;
	while (n-- != 0 && *d != '\0')
		d++;
	dlen = d - dst;
	n = siz - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));
}
